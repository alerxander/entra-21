package beans;

public class Beans2 {
	String nome;
	int quantidadeC, Vl, Cf, Ps, Pf;

	public int getQuantidadeC() {
		return quantidadeC;
	}

	public void setQuantidadeC(int quantidadeC) {
		this.quantidadeC = quantidadeC;
	}

	public int getVl() {
		return Vl;
	}

	public void setVl(int vl) {
		Vl = vl;
	}

	public int getCf() {
		return Cf;
	}

	public void setCf(int cf) {
		Cf = cf;
	}

	public int getPs() {
		return Ps;
	}

	public void setPs(int ps) {
		Ps = ps;
	}

	public int getPf() {
		return Pf;
	}

	public void setPf(int pf) {
		Pf = pf;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}
